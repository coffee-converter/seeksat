// scripts/build-fonts-embed.mjs - regenerate lib/og/fonts-embed.mjs.
//
// The OG rasterizer (lib/og/rasterize.mjs) needs the Arimo + Exo 2 TTFs
// at runtime in a Vercel serverless function. Reading them from disk is
// fragile: webpack inlines `__dirname` to the build path and the files
// aren't at that path in the deployed Lambda (ENOENT). Embedding the
// bytes as base64 in a bundled module sidesteps the filesystem entirely
// - the same trick lib/pass-finder/exo2-embed.js already uses for the
// wordmark woff2. Run this whenever the bundled fonts change.
import { readFileSync, writeFileSync } from "node:fs";
import { fileURLToPath } from "node:url";

const FONTS = ["Arimo-Regular", "Arimo-Bold", "Exo2-Regular", "Exo2-SemiBold"];
const dir = fileURLToPath(new URL("../lib/og/fonts/", import.meta.url));
const out = fileURLToPath(new URL("../lib/og/fonts-embed.mjs", import.meta.url));

const entries = FONTS.map((name) => {
  const b64 = readFileSync(`${dir}${name}.ttf`).toString("base64");
  return `  // ${name}.ttf\n  "${b64}",`;
}).join("\n");

const body = `// lib/og/fonts-embed.mjs - GENERATED by scripts/build-fonts-embed.mjs.
// The OG fonts (Arimo Regular/Bold, Exo 2 Regular/SemiBold) base64-encoded
// so lib/og/rasterize.mjs can load them with no filesystem access - robust
// in the bundled Vercel serverless function (see the script header). To
// change the fonts, edit lib/og/fonts/ and re-run the generator.
const BASE64 = [
${entries}
];

export const FONT_BUFFERS = BASE64.map((b) => Buffer.from(b, "base64"));
`;

writeFileSync(out, body);
const kb = Buffer.byteLength(body) / 1024;
console.log(`Wrote ${out} (${kb.toFixed(1)} KB, ${FONTS.length} fonts)`);
