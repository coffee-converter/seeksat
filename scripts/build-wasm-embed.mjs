// scripts/build-wasm-embed.mjs — regenerate lib/og/resvg-wasm-embed.mjs.
//
// The OG rasterizer uses @resvg/resvg-wasm (not the native @resvg/resvg-js)
// because the native Linux binary silently drops all text on Vercel even
// when fontBuffers are supplied, while the WASM build renders identically
// on every platform. resvg-wasm needs its ~2.4MB wasm bytes passed to
// initWasm(); shipping that file through a bundled serverless function via
// the filesystem is exactly the fragile path that produced ENOENT/__dirname
// bugs. So we embed the wasm (gzipped + base64) as a plain module — no
// filesystem, no file-tracing config, no platform variance. Run after
// bumping @resvg/resvg-wasm.
import { readFileSync, writeFileSync } from "node:fs";
import { gzipSync } from "node:zlib";
import { createRequire } from "node:module";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";

const require = createRequire(import.meta.url);
const wasmPath = join(dirname(require.resolve("@resvg/resvg-wasm")), "index_bg.wasm");
const out = fileURLToPath(new URL("../lib/og/resvg-wasm-embed.mjs", import.meta.url));

const wasm = readFileSync(wasmPath);
const b64 = gzipSync(wasm, { level: 9 }).toString("base64");

const body = `// lib/og/resvg-wasm-embed.mjs — GENERATED by scripts/build-wasm-embed.mjs.
// The @resvg/resvg-wasm binary, gzipped + base64, so lib/og/rasterize.mjs
// can initialise resvg with no filesystem access (robust in the bundled
// Vercel serverless function — see the script header). Regenerate after
// bumping @resvg/resvg-wasm.
import { gunzipSync } from "node:zlib";

const GZ_BASE64 =
  "${b64}";

export const RESVG_WASM = gunzipSync(Buffer.from(GZ_BASE64, "base64"));
`;

writeFileSync(out, body);
console.log(`Wrote ${out} (wasm ${(wasm.length / 1048576).toFixed(2)}MB -> ` +
  `${(Buffer.byteLength(body) / 1048576).toFixed(2)}MB module)`);
